    private void btnModificarArbitroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarArbitroActionPerformed
        Object obj = PanelModificarArbitro.getClientProperty("arbitroActual");

        if (obj instanceof ControladorArbitro controladorListaA) {
            if (campoTarjetasSacadas.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe completar la cantidad de tarjetas.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            try {
                controladorListaA.setCantTarjetas_Arbitro(Integer.parseInt(campoTarjetasSacadas.getText()));
                controladorListaA.setInternacional_Arbitro(checkInter.isSelected() ? 1 : 0);

                JOptionPane.showMessageDialog(this, "Datos del árbitro modificados con éxito.");
                PanelModificarArbitro.putClientProperty("arbitroActual", null);
                ((CardLayout) panelContenedor.getLayout()).show(panelContenedor, "PanelPrincipal");
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "El campo de tarjetas debe ser numérico.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
            InterfazArbitro.abrirVentana(controladorListaJ, controladorListaA);
            this.dispose();
    }//GEN-LAST:event_btnModificarArbitroActionPerformed

