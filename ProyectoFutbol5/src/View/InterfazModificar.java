    private void btnModificarJugadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarJugadorActionPerformed
        Object obj = PanelModificarJugador.getClientProperty("jugadorActual");

        if (obj instanceof ControladorJugador controladorListaJ) {
            if (campoPosicionJugador.getSelectedItem() == null ||
                campoGolesJugador.getText().trim().isEmpty() ||
                campoAmarillasJugador.getText().trim().isEmpty() ||
                campoRojasJugador.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Todos los campos deben estar completos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            try {
                controladorListaJ.setPosicion_Jugador(campoPosicionJugador.getSelectedItem().toString());
                controladorListaJ.setCantGoles_Jugador(Integer.parseInt(campoGolesJugador.getText()));
                controladorListaJ.setCantAmarillas_Jugador(Integer.parseInt(campoAmarillasJugador.getText()));
                controladorListaJ.setCantRojas_Jugador(Integer.parseInt(campoRojasJugador.getText()));

                JOptionPane.showMessageDialog(this, "Datos del jugador modificados con éxito.");
                PanelModificarJugador.putClientProperty("jugadorActual", null);
                ((CardLayout) panelContenedor.getLayout()).show(panelContenedor, "PanelPrincipal");
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Los campos de goles, amarillas y rojas deben ser numéricos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        InterfazJugador.abrirVentana(controladorListaJ, controladorListaA);
        this.dispose();
    }//GEN-LAST:event_btnModificarJugadorActionPerformed

