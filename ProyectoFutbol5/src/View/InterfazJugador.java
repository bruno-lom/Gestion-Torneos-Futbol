    private void btnCargarJugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarJugActionPerformed
    String equipoSeleccionado = campoSelEquipo.getSelectedItem().toString();
    int cantidadJugadores = 0;

    
    for (Jugador j : controladorListaJ.getListaJugador()) {
        if (j.getClubActual().equals(equipoSeleccionado)) {
            cantidadJugadores++;
        }
    }

    InterfazCarga.abrirVentanaJugador(controladorListaJ, controladorListaA);
    this.dispose();

    }//GEN-LAST:event_btnCargarJugActionPerformed

