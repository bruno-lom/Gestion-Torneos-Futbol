     public static void abrirVentana(ControladorJugador controladorJ, ControladorArbitro controladorA, String dni) {
        Jugador jugador = controladorJ.buscarPorDNI(dni);
        Arbitro arbitro = controladorA.buscarPorDNI(dni);
    

        if (jugador == null && arbitro == null) {
            JOptionPane.showMessageDialog(null, "No se encontró una persona con ese documento.", "No encontrado", JOptionPane.WARNING_MESSAGE);
            
            InterfazPrincipal ventanaPrincipal = new InterfazPrincipal(controladorJ, controladorA);  
            ventanaPrincipal.setVisible(true);
    
            return;
        }

        InterfazModificar ventana = new InterfazModificar();
        ventana.controladorListaJ = controladorJ;
        ventana.controladorListaA = controladorA;

        if (jugador != null) {
            ventana.cargarJugador(jugador);
        } else {
            ventana.cargarArbitro(arbitro);
        }

        ventana.setVisible(true);
        
    }


