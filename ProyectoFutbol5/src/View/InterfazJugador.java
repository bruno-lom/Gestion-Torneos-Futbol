    private void campoSelEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campoSelEquipoActionPerformed
        String equipoSeleccionado = campoSelEquipo.getSelectedItem().toString();

        if (equipoSeleccionado.equals("Seleccione un equipo") || equipoSeleccionado.trim().isEmpty()) {

            TablaJugadores.setModel(new DefaultListModel<>()); // limpia la lista
            return;
        }

        int contador = 0;
        DefaultListModel<String> modelo = new DefaultListModel<>();

        List<Jugador> lista = controladorListaJ.getListaJugador();

        for (Jugador j : lista) {
            if (j.getClubActual().equalsIgnoreCase(equipoSeleccionado)) {
                String datos = j.getNombre() + " " + j.getApellido() + " - DNI: " + j.getNumDoc();
                modelo.addElement(datos);
                contador++;
            }
        }

        TablaJugadores.setModel(modelo);

    }//GEN-LAST:event_campoSelEquipoActionPerformed

    private void btnModJugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModJugActionPerformed
        String jugadorSeleccionado = TablaJugadores.getSelectedValue();

        if (jugadorSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Seleccioná un jugador de la lista.");
            return;
        }

        String[] partes = jugadorSeleccionado.split("DNI: ");
        if (partes.length == 2) {
            String dni = partes[1].trim();

            
            InterfazModificar.abrirVentana(controladorListaJ, controladorListaA, dni);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo obtener el DNI del jugador seleccionado.");
        }
    }//GEN-LAST:event_btnModJugActionPerformed

