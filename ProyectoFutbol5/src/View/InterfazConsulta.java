    private void btnBusGolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusGolActionPerformed
  String golesTexto = campoCantGoles.getText().trim();

if (golesTexto.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Ingrese una cantidad de goles válida.");
    return;
}

int golesMinimos;

try {
    golesMinimos = Integer.parseInt(golesTexto);
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Debe ingresar un número entero.");
    return;
}

List<Jugador> lista = controladorListaJ.getListaJugador();

lista.sort((j1, j2) -> Integer.compare(j2.getCantGoles(), j1.getCantGoles()));

DefaultListModel<String> modeloLista = new DefaultListModel<>();

for (Jugador j : lista) {
    if (j.getCantGoles() >= golesMinimos) {
        String info = j.getNombre() + " " + j.getApellido() + " - Goles: " + j.getCantGoles();
        modeloLista.addElement(info);
    }
}

if (modeloLista.isEmpty()) {
    modeloLista.addElement("No hay jugadores con esa cantidad de goles o más.");
}

TablaGoleadores.setModel(modeloLista);

    }//GEN-LAST:event_btnBusGolActionPerformed

