    private void btnJugMasExpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJugMasExpActionPerformed

   List<Jugador> lista = controladorListaJ.getListaJugador();

    if (lista.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay jugadores cargados.");
        return;
    }

    int maxRojas = 0;
    for (Jugador j : lista) {
        if (j.getCantRojas() > maxRojas) {
            maxRojas = j.getCantRojas();
        }
    }

    DefaultListModel<String> modeloExpulsados = new DefaultListModel<>();
    for (Jugador j : lista) {
        if (j.getCantRojas() == maxRojas) {
            String info = j.getNombre() + " " + j.getApellido() +
                          " - Rojas: " + j.getCantRojas() +
                          " - Equipo: " + j.getClubActual();
            modeloExpulsados.addElement(info);
        }
    }

    TablaMasExpulsados.setModel(modeloExpulsados);

    
    CardLayout cl = (CardLayout) PanelContenedor.getLayout();
    cl.show(PanelContenedor, "PanelJugMasExp");


    }//GEN-LAST:event_btnJugMasExpActionPerformed

